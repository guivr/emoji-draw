export function getDefaultPlacementStep(placementIndex: number) {
  const sequenceLength = DEFAULT_TENSION_SEQUENCE.length;
  if (sequenceLength === 0) return 0;
  return Math.max(0, placementIndex) % sequenceLength;
}

export function getMinPlayIntervalForEmoji(emoji: string) {
  return EMOJI_MIN_PLAY_INTERVAL_OVERRIDES[emoji] ?? DEFAULT_MIN_PLAY_INTERVAL_MS;
}

function getRandomVariantStep(variantCount: number) {
  if (variantCount <= 1) return 0;
  return Math.min(variantCount - 1, Math.floor(Math.random() * variantCount));
}

export function getSoundSourceForPlacement(emoji: string, placementIndex: number): {
  source: AudioSource;
  cacheKey: string;
} {
  const override = EMOJI_SOUND_OVERRIDES[emoji];
  if (override) {
    if (Array.isArray(override)) {
      const variantStep = getRandomVariantStep(override.length);
      return {
        source: override[variantStep],
        cacheKey: `emoji:${emoji}:${variantStep}`,
      };
    }

    return {
      source: override,
      cacheKey: `emoji:${emoji}`,
    };
  }

  const step = getDefaultPlacementStep(placementIndex);
  return {
    source: DEFAULT_TENSION_SEQUENCE[step],
    cacheKey: `default:${step}`,
  };
}
